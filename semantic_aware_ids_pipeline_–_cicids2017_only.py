def prepare_cicids_subset(
    max_per_class: int = 20000,
    csv_path: str = None,
    csv_paths=None,
    random_state: int = RANDOM_STATE,
):
    """
    Load CICIDS2017, clean labels, build a stratified subset,
    and save it to disk.

    Parameters
    ----------
    max_per_class : int
        Max number of samples per label.
    csv_path : str
        Path to a single aggregated CICIDS file.
    csv_paths : list[str] or None
        Optional list of raw CSV files (e.g., the 8 original days).
        If provided, these are concatenated. If None, csv_path is used.
    """
    dataset_name = "CICIDS2017"

    # ----- Load raw data -----
    if csv_paths is not None:
        # List of CSV files
        dfs = []
        for p in csv_paths:
            print(f"[{dataset_name}] Reading file: {p}")
            dfs.append(pd.read_csv(p, low_memory=False))
        df = pd.concat(dfs, ignore_index=True)
    else:
        if csv_path is None:
            csv_path = DEFAULT_CICIDS_AGG_PATH
        print(f"[{dataset_name}] Reading aggregated CSV: {csv_path}")
        df = pd.read_csv(csv_path, low_memory=False)

    # Normalize column names (lowercase + underscores)
    df.columns = [c.strip().lower().replace(" ", "_") for c in df.columns]

    # ----- Label handling -----
    if "label" not in df.columns:
        label_col = find_label_column(df)
        if label_col is None:
            raise ValueError(f"{dataset_name}: no label-like column found.")
        df["label"] = df[label_col].astype(str)
        label_col = "label"
    else:
        label_col = "label"
        df["label"] = df["label"].astype(str)

    # Clean label strings
    df["label"] = df["label"].str.strip()

    # Remove parasitic rows where label is literally "label"
    mask_bad = df["label"].str.lower() == "label"
    if mask_bad.any():
        print(f"[{dataset_name}] Removing {mask_bad.sum()} parasitic rows with label='label'")
        df = df.loc[~mask_bad].reset_index(drop=True)

    # Quick inspection
    label_col = inspect_dataset(df, dataset_name=dataset_name, label_col=label_col)

    # ----- Stratified sampling -----
    subset_df, out_path = stratified_sample_and_save(
        df,
        dataset_name=dataset_name,
        label_col=label_col,
        max_per_class=max_per_class,
        subset_dir=BASE_SUBSET_DIR,
        random_state=random_state,
    )

    return subset_df, out_path, label_col


