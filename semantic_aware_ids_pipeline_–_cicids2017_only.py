def get_or_compute_embeddings(text_list, base_dir, split_name, encoder, batch_size=256):
    """
    text_list : iterable de textes
    base_dir  : dossier où sauvegarder/charger les embeddings
    split_name: 'train', 'val', 'test'
    encoder   : SentenceTransformer déjà initialisé
    """
    os.makedirs(base_dir, exist_ok=True)
    cache_path = os.path.join(base_dir, f"{split_name}_embeddings.npy")

    # Rechargement éventuel
    if os.path.exists(cache_path):
        emb = np.load(cache_path)
        if emb.shape[0] == len(text_list):
            print(f"[CACHE] Loaded embeddings from {cache_path} with shape {emb.shape}")
            return emb
        else:
            print(
                f"[CACHE] Shape mismatch for {cache_path} "
                f"(cached={emb.shape[0]}, expected={len(text_list)}). Recomputing..."
            )

    print(f"[ENCODER] Computing embeddings for split='{split_name}' ({len(text_list)} samples)...")
    texts = list(text_list)
    emb = encoder.encode(texts, show_progress_bar=True, batch_size=batch_size)

    np.save(cache_path, emb)
    print(f"[CACHE] Saved embeddings to {cache_path} with shape {emb.shape}")
    return emb

