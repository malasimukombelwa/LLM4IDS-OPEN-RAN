def cic_row_to_text(row):
    """
    Text narration for one CICIDS2017 flow.
    Uses main network fields + label.
    """
    parts = []
    # Network identifiers
    for c, label in [
        ("protocol", "Protocol"),
        ("protocol_name", "ProtocolName"),
        ("flow_id", "FlowID"),
        ("source_ip", "SrcIP"),
        ("destination_ip", "DstIP"),
        ("source_port", "Sport"),
        ("destination_port", "Dport"),
    ]:
        if c in row.index:
            parts.append(f"{label}={row[c]}.")

    # Main stats
    for c in ["total_fwd_packets", "total_backward_packets",
              "flow_duration", "flow_bytes/s", "flow_packets/s"]:
        if c in row.index:
            parts.append(f"{c}={row[c]}.")

    # Label
    if "label" in row.index:
        parts.append(f"Flow labelled as {row['label']}.")

    return " ".join(str(p) for p in parts)

def load_cicids_dataset(use_unified_labels: bool = False):
    """
    Load CICIDS2017 from the aggregated CSV, clean it, build NUM/TEXT features,
    and optionally remap labels to a unified taxonomy.

    Parameters
    ----------
    use_unified_labels : bool
        If True, original labels (BENIGN, DoS Hulk, ...) are remapped into
        coarse semantic classes (normal, dos_ddos, scan_recon, auth_web,
        compromise_bot_iot, other_attack). Original labels are kept in
        column 'label_orig' for analysis.
    """
    print("=== Loading CICIDS2017 aggregated file ===")
    df = pd.read_csv(CICIDS_CSV, low_memory=False)

    # 1) Normalize column names
    df.columns = [c.strip().lower().replace(" ", "_") for c in df.columns]

    # 2) Remove internal header rows (classic CICIDS issue)
    if "flow_id" in df.columns:
        mask_header_rows = df["flow_id"].astype(str).str.lower().str.contains("flow_id")
        if mask_header_rows.any():
            print(f"[CICIDS2017] Removing {mask_header_rows.sum()} internal header rows")
            df = df.loc[~mask_header_rows].reset_index(drop=True)

    # 3) Label handling
    if "label" not in df.columns:
        raise ValueError("Column 'label' not found in CICIDS2017 – check the file.")
    df["label"] = df["label"].astype(str).str.strip()

    # Remove rows where label == "label"
    mask_bad = df["label"].str.lower() == "label"
    if mask_bad.any():
        print(f"[CICIDS2017] Removing {mask_bad.sum()} rows with label='label'")
        df = df.loc[~mask_bad].reset_index(drop=True)

    # --- Optionnel : remapping vers la taxonomie unifiée ---
    if use_unified_labels:
        print("\n[CICIDS2017] Using unified taxonomy for labels.")
        df["label_orig"] = df["label"]  # garder les labels fins pour analyse

        def map_label(lbl: str) -> str:
            return CICIDS_UNIFIED_MAP.get(lbl, "other_attack")

        df["label"] = df["label_orig"].map(map_label)

        print("[CICIDS2017] Original label distribution (top 20):")
        print(df["label_orig"].value_counts().head(20))

        print("\n[CICIDS2017] Unified label distribution:")
        print(df["label"].value_counts())
    else:
        print("[CICIDS2017] Label distribution (top 20):")
        print(df["label"].value_counts().head(20))

    # 4) Columns to exclude from numeric features
    id_like = [
        c for c in df.columns
        if any(k in c for k in [
            "flow_id", "source_ip", "destination_ip", "src_ip", "dst_ip",
            "timestamp", "time", "date"
        ])
    ]
    to_exclude = set(id_like + ["label"])

    # 5) Candidate numeric columns
    candidate_cols = df.drop(columns=list(to_exclude), errors="ignore").columns

    # Explicit numeric coercion
    for col in candidate_cols:
        df[col] = pd.to_numeric(df[col], errors="coerce")

    # 6) Final numeric feature columns
    num_cols = df[candidate_cols].select_dtypes(include=[np.number]).columns.tolist()
    if len(num_cols) == 0:
        raise ValueError("No numeric columns found for CICIDS2017 even after coercion – check file format.")

    print(f"[CICIDS2017] Numeric feature cols ({len(num_cols)}): {num_cols[:10]} ...")

    # 7) Numeric cleaning (inf, NaN, clipping)
    num_df = df[num_cols]
    num_df = num_df.replace([np.inf, -np.inf], np.nan)
    num_df = num_df.clip(lower=-1e9, upper=1e9)
    num_df = num_df.fillna(0.0)

    X_num = num_df.values
    y = df["label"].values   # ici : labels originaux OU unifiés, selon le flag

    # 8) Text (narrative flows)
    df["text"] = df.apply(cic_row_to_text, axis=1)
    X_text = df["text"].values

    # 9) Train/val/test split (aligned NUM + TEXT) with stratify
    X_train_num, X_temp_num, X_train_text, X_temp_text, y_train, y_temp = train_test_split(
        X_num,
        X_text,
        y,
        test_size=0.3,
        random_state=RANDOM_STATE,
        stratify=y
    )
    X_val_num, X_test_num, X_val_text, X_test_text, y_val, y_test = train_test_split(
        X_temp_num,
        X_temp_text,
        y_temp,
        test_size=0.5,
        random_state=RANDOM_STATE,
        stratify=y_temp
    )

    # 10) Standardization
    scaler = StandardScaler()
    X_train_num = scaler.fit_transform(X_train_num)
    X_val_num   = scaler.transform(X_val_num)
    X_test_num  = scaler.transform(X_test_num)

    # 11) Load precomputed embeddings (MiniLM) + fusion
    X_train_emb = np.load(TRAIN_EMB_PATH)
    X_val_emb   = np.load(VAL_EMB_PATH)
    X_test_emb  = np.load(TEST_EMB_PATH)

    print("Embeddings vs numeric shapes:", X_train_emb.shape, X_train_num.shape)
    assert X_train_emb.shape[0] == X_train_num.shape[0], "Train emb/num mismatch"
    assert X_val_emb.shape[0]   == X_val_num.shape[0],   "Val emb/num mismatch"
    assert X_test_emb.shape[0]  == X_test_num.shape[0],  "Test emb/num mismatch"

    X_train_comb = np.concatenate([X_train_emb, X_train_num], axis=1)
    X_val_comb   = np.concatenate([X_val_emb,   X_val_num],   axis=1)
    X_test_comb  = np.concatenate([X_test_emb,  X_test_num],  axis=1)

    # 12) Label encoding
    le = LabelEncoder()
    y_train_int = le.fit_transform(y_train)
    y_val_int   = le.transform(y_val)
    y_test_int  = le.transform(y_test)

    y_train_oh = tf.keras.utils.to_categorical(y_train_int)
    y_val_oh   = tf.keras.utils.to_categorical(y_val_int)
    y_test_oh  = tf.keras.utils.to_categorical(y_test_int)

    print(f"[CICIDS2017] Shapes – X_train_comb: {X_train_comb.shape}, "
          f"X_val_comb: {X_val_comb.shape}, X_test_comb: {X_test_comb.shape}")
    print(f"[CICIDS2017] Classes : {list(le.classes_)}")

    return {
        "name": "CICIDS2017" if not use_unified_labels else "CICIDS2017_unified",
        "num_cols": num_cols,
        "scaler": scaler,
        "le": le,
        "X_train_text": np.array(X_train_text),
        "X_val_text": np.array(X_val_text),
        "X_test_text": np.array(X_test_text),
        "X_train_num": X_train_num,
        "X_val_num": X_val_num,
        "X_test_num": X_test_num,
        "X_train_emb": X_train_emb,
        "X_val_emb": X_val_emb,
        "X_test_emb": X_test_emb,
        "X_train_comb": X_train_comb,
        "X_val_comb": X_val_comb,
        "X_test_comb": X_test_comb,
        "y_train": y_train_int,
        "y_val": y_val_int,
        "y_test": y_test_int,
        "y_train_oh": y_train_oh,
        "y_val_oh": y_val_oh,
        "y_test_oh": y_test_oh,
    }

