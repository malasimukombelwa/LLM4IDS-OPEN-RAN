def measure_inference_time(model, X, keras=False, n_repeats=3):
    """Temps moyen d'inférence par échantillon (ms)."""
    times = []
    for _ in range(n_repeats):
        start = time.time()
        if keras:
            _ = model.predict(X, verbose=0)
        else:
            _ = model.predict(X)
        end = time.time()
        times.append(end - start)
    avg_total = np.mean(times)
    return round((avg_total / X.shape[0]) * 1000.0, 3)

