def eval_model_unv(model, X_test, y_test, le=None,
                   model_name="Model", keras=False,
                   outdir=None, plot_curves=False):
    """
    Universal multiclass evaluation (sklearn or Keras).
    Returns a metrics dict + objects for curves.
    """
    # Harmonize y_test
    y_true = np.array(y_test)
    if len(y_true.shape) > 1 and y_true.shape[1] > 1:
        y_true = np.argmax(y_true, axis=1)

    # Predictions
    if keras:
        y_score = model.predict(X_test, verbose=0)
        y_pred = np.argmax(y_score, axis=1)
    else:
        if hasattr(model, "predict_proba"):
            y_score = model.predict_proba(X_test)
        else:
            y_score = None
        y_pred = model.predict(X_test)

    # Label encoding / decoding
    if le is not None:
        if isinstance(y_true[0], str):
            y_true_int = le.transform(y_true)
        else:
            y_true_int = y_true

        if isinstance(y_pred[0], str):
            y_pred_int = le.transform(y_pred)
        else:
            y_pred_int = y_pred

        y_true_lbl = le.inverse_transform(y_true_int)
        y_pred_lbl = le.inverse_transform(y_pred_int)
        class_names = le.classes_
    else:
        y_true_int = y_true
        y_pred_int = y_pred
        y_true_lbl = y_true
        y_pred_lbl = y_pred
        class_names = np.unique(y_true)

    print(f"\n=== Classification report – {model_name} ===")
    print(classification_report(y_true_lbl, y_pred_lbl, digits=4))

    cm = confusion_matrix(y_true_lbl, y_pred_lbl, labels=class_names)

    conf_path = None
    if outdir is not None:
        safe_name = model_name.replace(" ", "_")
        conf_path = os.path.join(outdir, f"{safe_name}_confusion.png")

    plot_confusion(
        cm, class_names,
        title=f"Confusion – {model_name}",
        savepath=conf_path,
        show=True,
    )

    # Global metrics
    acc = accuracy_score(y_true_int, y_pred_int)
    prec = precision_score(y_true_int, y_pred_int, average="weighted", zero_division=0)
    rec  = recall_score(y_true_int, y_pred_int, average="weighted", zero_division=0)
    f1_weighted = f1_score(y_true_int, y_pred_int, average="weighted", zero_division=0)
    f1_macro    = f1_score(y_true_int, y_pred_int, average="macro",    zero_division=0)

    # LogLoss + confidence + ECE if probabilities are available
    logloss = None
    ece = None
    avg_conf = avg_conf_correct = avg_conf_incorrect = None

    if y_score is not None:
        try:
            logloss = log_loss(y_true_int, y_score, labels=np.arange(len(class_names)))
        except ValueError:
            logloss = None

        confidences = np.max(y_score, axis=1)
        correct_mask = (y_true_int == y_pred_int)

        avg_conf = float(np.mean(confidences))
        if np.any(correct_mask):
            avg_conf_correct = float(np.mean(confidences[correct_mask]))
        if np.any(~correct_mask):
            avg_conf_incorrect = float(np.mean(confidences[~correct_mask]))

        ece = compute_ece(y_true_int, y_score, n_bins=15)

        if plot_curves and outdir is not None:
            plot_multiclass_roc_pr(y_true_int, y_score, le, model_name, outdir)

    metrics = {
        "Model": model_name,
        "Accuracy": acc,
        "Precision": prec,
        "Recall": rec,
        "F1": f1_weighted,        # weighted F1
        "F1_macro": f1_macro,     # macro F1
        "LogLoss": logloss,
        "ECE": ece,
        "AvgConf": avg_conf,
        "AvgConf_correct": avg_conf_correct,
        "AvgConf_incorrect": avg_conf_incorrect,
    }

    return metrics, cm, class_names, y_true_int, y_pred_int, y_score

