def compute_ece(y_true_int, y_score, n_bins=15):
    """
    Simplified Expected Calibration Error (ECE) based on max confidence.
    """
    confidences = np.max(y_score, axis=1)
    preds = np.argmax(y_score, axis=1)
    correctness = (preds == y_true_int).astype(int)

    bin_edges = np.linspace(0.0, 1.0, n_bins + 1)
    ece = 0.0

    for i in range(n_bins):
        bin_lower, bin_upper = bin_edges[i], bin_edges[i + 1]
        in_bin = (confidences > bin_lower) & (confidences <= bin_upper) if i > 0 else \
                 (confidences >= bin_lower) & (confidences <= bin_upper)
        prop_in_bin = np.mean(in_bin)
        if prop_in_bin > 0:
            acc_in_bin = np.mean(correctness[in_bin])
            conf_in_bin = np.mean(confidences[in_bin])
            ece += np.abs(acc_in_bin - conf_in_bin) * prop_in_bin

    return float(ece)


def plot_multiclass_roc_pr(y_true_int, y_score, le, model_name, outdir):
    """
    Plot and save multiclass ROC and PR curves
    (micro + macro + per-class).
    """
    if y_score is None:
        print(f"[WARN] No probability scores for {model_name}, skipping ROC/PR curves.")
        return

    y_true_int = np.asarray(y_true_int)
    y_score = np.asarray(y_score, dtype=float)

    if y_score.ndim != 2:
        raise ValueError(f"[{model_name}] y_score must be (n_samples, n_classes), got {y_score.shape}")
    if y_true_int.shape[0] != y_score.shape[0]:
        raise ValueError(
            f"[{model_name}] y_true and y_score mismatch: "
            f"{y_true_int.shape[0]} vs {y_score.shape[0]}"
        )

    n_classes = len(le.classes_)
    classes_range = np.arange(n_classes)

    # Sanity check from probabilities
    preds_from_proba = np.argmax(y_score, axis=1)
    acc_check = accuracy_score(y_true_int, preds_from_proba)
    print(f"[{model_name}] Accuracy from probabilities = {acc_check:.4f}")

    # Binarize labels
    y_bin = label_binarize(y_true_int, classes=classes_range)

    # ---------- ROC ----------
    fpr, tpr, roc_auc = {}, {}, {}
    for i in range(n_classes):
        fpr[i], tpr[i], _ = roc_curve(y_bin[:, i], y_score[:, i])
        roc_auc[i] = auc(fpr[i], tpr[i])

    fpr["micro"], tpr["micro"], _ = roc_curve(y_bin.ravel(), y_score.ravel())
    roc_auc["micro"] = auc(fpr["micro"], tpr["micro"])

    all_fpr = np.unique(np.concatenate([fpr[i] for i in range(n_classes)]))
    mean_tpr = np.zeros_like(all_fpr)
    for i in range(n_classes):
        mean_tpr += np.interp(all_fpr, fpr[i], tpr[i])
    mean_tpr /= n_classes
    roc_auc["macro"] = auc(all_fpr, mean_tpr)

    plt.figure(figsize=(7, 6))
    plt.plot(
        fpr["micro"], tpr["micro"], linestyle=":",
        label=f"micro-average (AUC={roc_auc['micro']:.4f})"
    )
    plt.plot(
        all_fpr, mean_tpr,
        label=f"macro-average (AUC={roc_auc['macro']:.4f})"
    )
    for i in range(n_classes):
        plt.plot(
            fpr[i], tpr[i],
            alpha=0.3,
            label=f"{le.classes_[i]} (AUC={roc_auc[i]:.3f})"
        )
    plt.plot([0, 1], [0, 1], "--", color="gray")
    plt.xlim([0.0, 1.0])
    plt.ylim([0.0, 1.05])
    plt.xlabel("False Positive Rate")
    plt.ylabel("True Positive Rate")
    plt.title(f"ROC curves – {model_name}")
    plt.legend(loc="lower right", fontsize=8)
    roc_path = os.path.join(outdir, f"{model_name}_ROC_multiclass.png")
    plt.savefig(roc_path, dpi=300, bbox_inches="tight")
    plt.show()
    plt.close()

    # ---------- Precision-Recall ----------
    precision_d, recall_d, pr_auc = {}, {}, {}
    ap_scores = {}

    for i in range(n_classes):
        precision_d[i], recall_d[i], _ = precision_recall_curve(
            y_bin[:, i], y_score[:, i]
        )
        pr_auc[i] = auc(recall_d[i], precision_d[i])
        ap_scores[i] = average_precision_score(y_bin[:, i], y_score[:, i])

    precision_d["micro"], recall_d["micro"], _ = precision_recall_curve(
        y_bin.ravel(), y_score.ravel()
    )
    pr_auc["micro"] = auc(recall_d["micro"], precision_d["micro"])
    ap_micro = average_precision_score(y_bin, y_score, average="micro")

    print(f"[{model_name}] Micro PR-AUC = {pr_auc['micro']:.4f}, AP_micro = {ap_micro:.4f}")
    for i in range(n_classes):
        print(
            f"  Class {le.classes_[i]}: PR-AUC={pr_auc[i]:.4f}, "
            f"AP={ap_scores[i]:.4f}"
        )

    plt.figure(figsize=(7, 6))
    plt.plot(
        recall_d["micro"], precision_d["micro"], linestyle=":",
        label=f"micro-average (AUC={pr_auc['micro']:.4f})"
    )
    for i in range(n_classes):
        plt.plot(
            recall_d[i], precision_d[i],
            alpha=0.3,
            label=f"{le.classes_[i]} (AUC={pr_auc[i]:.3f})"
        )
    plt.xlim([0.0, 1.0])
    plt.ylim([0.0, 1.05])
    plt.xlabel("Recall")
    plt.ylabel("Precision")
    plt.title(f"Precision–Recall curves – {model_name}")
    plt.legend(loc="lower left", fontsize=8)
    pr_path = os.path.join(outdir, f"{model_name}_PR_multiclass.png")
    plt.savefig(pr_path, dpi=300, bbox_inches="tight")
    plt.show()
    plt.close()


def eval_model_unv(model, X_test, y_test, le=None,
                   model_name="Model", keras=False,
                   outdir=None, plot_curves=False):
    """
    Universal multiclass evaluation (sklearn or Keras).
    Returns a metrics dict + objects for curves.
    """
    # Harmonize y_test
    y_true = np.array(y_test)
    if len(y_true.shape) > 1 and y_true.shape[1] > 1:
        y_true = np.argmax(y_true, axis=1)

    # Predictions
    if keras:
        y_score = model.predict(X_test, verbose=0)
        y_pred = np.argmax(y_score, axis=1)
    else:
        if hasattr(model, "predict_proba"):
            y_score = model.predict_proba(X_test)
        else:
            y_score = None
        y_pred = model.predict(X_test)

    # Label encoding / decoding
    if le is not None:
        if isinstance(y_true[0], str):
            y_true_int = le.transform(y_true)
        else:
            y_true_int = y_true

        if isinstance(y_pred[0], str):
            y_pred_int = le.transform(y_pred)
        else:
            y_pred_int = y_pred

        y_true_lbl = le.inverse_transform(y_true_int)
        y_pred_lbl = le.inverse_transform(y_pred_int)
        class_names = le.classes_
    else:
        y_true_int = y_true
        y_pred_int = y_pred
        y_true_lbl = y_true
        y_pred_lbl = y_pred
        class_names = np.unique(y_true)

    print(f"\n=== Classification report – {model_name} ===")
    print(classification_report(y_true_lbl, y_pred_lbl, digits=4))

    cm = confusion_matrix(y_true_lbl, y_pred_lbl, labels=class_names)

    conf_path = None
    if outdir is not None:
        safe_name = model_name.replace(" ", "_")
        conf_path = os.path.join(outdir, f"{safe_name}_confusion.png")

    plot_confusion(
        cm, class_names,
        title=f"Confusion – {model_name}",
        savepath=conf_path,
        show=True,
    )

    # Global metrics
    acc = accuracy_score(y_true_int, y_pred_int)
    prec = precision_score(y_true_int, y_pred_int, average="weighted", zero_division=0)
    rec  = recall_score(y_true_int, y_pred_int, average="weighted", zero_division=0)
    f1_weighted = f1_score(y_true_int, y_pred_int, average="weighted", zero_division=0)
    f1_macro    = f1_score(y_true_int, y_pred_int, average="macro",    zero_division=0)

    # LogLoss + confidence + ECE if probabilities are available
    logloss = None
    ece = None
    avg_conf = avg_conf_correct = avg_conf_incorrect = None

    if y_score is not None:
        try:
            logloss = log_loss(y_true_int, y_score, labels=np.arange(len(class_names)))
        except ValueError:
            logloss = None

        confidences = np.max(y_score, axis=1)
        correct_mask = (y_true_int == y_pred_int)

        avg_conf = float(np.mean(confidences))
        if np.any(correct_mask):
            avg_conf_correct = float(np.mean(confidences[correct_mask]))
        if np.any(~correct_mask):
            avg_conf_incorrect = float(np.mean(confidences[~correct_mask]))

        ece = compute_ece(y_true_int, y_score, n_bins=15)

        if plot_curves and outdir is not None:
            plot_multiclass_roc_pr(y_true_int, y_score, le, model_name, outdir)

    metrics = {
        "Model": model_name,
        "Accuracy": acc,
        "Precision": prec,
        "Recall": rec,
        "F1": f1_weighted,        # weighted F1
        "F1_macro": f1_macro,     # macro F1
        "LogLoss": logloss,
        "ECE": ece,
        "AvgConf": avg_conf,
        "AvgConf_correct": avg_conf_correct,
        "AvgConf_incorrect": avg_conf_incorrect,
    }

    return metrics, cm, class_names, y_true_int, y_pred_int, y_score

