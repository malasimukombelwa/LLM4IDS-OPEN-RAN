def visualize_cicids(
    df: pd.DataFrame,
    label_col: str = "label",
    max_points_scatter: int = 50000,
    save_dir: str = None,
):
    if save_dir is not None:
        ensure_dir(save_dir)

    dataset_name = "CICIDS2017"

    # 1) Label distribution
    if label_col in df.columns:
        vc = df[label_col].value_counts()
        print(f"\n[{dataset_name}] Label distribution:")
        print(vc.head(40))

        if 1 < vc.shape[0] <= 40:
            plt.figure(figsize=(10, 4))
            sns.countplot(x=label_col, data=df, order=vc.index)
            plt.title(f"{dataset_name} – Label distribution")
            plt.xticks(rotation=75, ha="right")
            plt.tight_layout()
            if save_dir is not None:
                out = os.path.join(save_dir, f"{dataset_name}_labels.png")
                plt.savefig(out, dpi=300, bbox_inches="tight")
                print(f" Label figure saved to: {out}")
            plt.show()
    else:
        print(f"[{dataset_name}] No label column, skipping label distribution.")
        label_col = None

    # 2) Main KPIs
    kpi_cols = auto_select_kpi_cols_nids(df, max_cols=6)
    if not kpi_cols:
        print(f"[{dataset_name}] No numeric columns to plot.")
    else:
        print(f"\n[{dataset_name}] Selected KPI columns: {kpi_cols}")

        n_cols = 3
        n_rows = int(np.ceil(len(kpi_cols) / n_cols))

        plt.figure(figsize=(4 * n_cols, 3.2 * n_rows))
        for i, col in enumerate(kpi_cols, 1):
            plt.subplot(n_rows, n_cols, i)
            if (
                label_col is not None
                and label_col in df.columns
                and df[label_col].nunique() > 1
            ):
                sns.histplot(
                    data=df,
                    x=col,
                    hue=label_col,
                    kde=False,
                    bins=40,
                    stat="count",
                )
            else:
                sns.histplot(df[col].dropna(), kde=False, bins=40)
            plt.title(col)
            plt.tight_layout()

        if save_dir is not None:
            out = os.path.join(save_dir, f"{dataset_name}_kpi_hist.png")
            plt.savefig(out, dpi=300, bbox_inches="tight")
            print(f" KPI hist figure saved to: {out}")
        plt.show()

    # 3) Scatter (duration vs bytes/packets)
    scatter_x, scatter_y = pick_scatter_cols(
        df,
        priority_x=["dur", "time", "flow_duration"],
        priority_y=["byte", "bytes", "tot", "pkt", "pkts"],
    )

    if scatter_x is None or scatter_y is None:
        print(f"[{dataset_name}] No obvious (x, y) pair for scatter.")
        return

    print(f"[{dataset_name}] Scatter: x={scatter_x}, y={scatter_y}")

    cols = [scatter_x, scatter_y]
    if label_col in df.columns:
        cols.append(label_col)
    df_plot = df[cols].dropna()

    if len(df_plot) > max_points_scatter:
        df_plot = df_plot.sample(n=max_points_scatter, random_state=RANDOM_STATE)

    plt.figure(figsize=(6, 5))
    if label_col in df_plot.columns and df_plot[label_col].nunique() > 1:
        sns.scatterplot(
            data=df_plot,
            x=scatter_x,
            y=scatter_y,
            hue=label_col,
            alpha=0.5,
        )
        plt.legend(title=label_col, bbox_to_anchor=(1.05, 1), loc="upper left")
    else:
        plt.scatter(df_plot[scatter_x], df_plot[scatter_y], alpha=0.5)

    plt.xlabel(scatter_x)
    plt.ylabel(scatter_y)
    plt.title(f"{dataset_name} – {scatter_x} vs {scatter_y}")
    plt.tight_layout()
    if save_dir is not None:
        out = os.path.join(save_dir, f"{dataset_name}_scatter.png")
        plt.savefig(out, dpi=300, bbox_inches="tight")
        print(f" Scatter figure saved to: {out}")
    plt.show()


