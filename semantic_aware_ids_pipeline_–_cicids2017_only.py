def find_label_column(df: pd.DataFrame):
    """
    Try to guess the label column in CICIDS-like data.
    Assumes columns were already normalized to lowercase_with_underscores.
    """
    candidate_keywords = ["label", "attack", "class", "category"]
    for c in df.columns:
        cl = c.lower()
        if any(k in cl for k in candidate_keywords):
            return c
    # Fallback: non-numeric column with few distinct values
    for c in df.columns:
        if not np.issubdtype(df[c].dtype, np.number) and df[c].nunique() < 50:
            return c
    return None


def inspect_dataset(df: pd.DataFrame, dataset_name: str, label_col: str):
    print(f"[{dataset_name}] shape: {df.shape}")
    print(f"[{dataset_name}] dtypes (head):")
    print(df.dtypes.head(20))

    if label_col in df.columns:
        print(f"\n[{dataset_name}] Label distribution (top 30):")
        print(df[label_col].value_counts().head(30))
    else:
        print(f"[{dataset_name}] No label column found during inspection.")
    return label_col


def stratified_sample_and_save(
    df: pd.DataFrame,
    dataset_name: str,
    label_col: str,
    max_per_class: int,
    subset_dir: str,
    random_state: int = 42,
):
    """
    For each class in label_col, take up to max_per_class samples.
    Save to CSV and return (subset_df, csv_path).
    """
    ensure_dir(subset_dir)

    groups = []
    for lab, grp in df.groupby(label_col):
        n = min(max_per_class, len(grp))
        print(f"[{dataset_name}] Label='{lab}': taking {n}/{len(grp)} rows")
        groups.append(grp.sample(n=n, random_state=random_state))

    subset_df = pd.concat(groups, ignore_index=True)
    subset_df = subset_df.sample(frac=1.0, random_state=random_state).reset_index(drop=True)

    out_path = os.path.join(
        subset_dir,
        f"{dataset_name}_subset_max{max_per_class}.csv"
    )
    subset_df.to_csv(out_path, index=False)
    print(f"[{dataset_name}] Stratified subset saved to: {out_path}")

    return subset_df, out_path


def auto_select_kpi_cols_nids(df, max_cols: int = 6):
    """Select a few meaningful traffic KPIs among numeric columns."""
    num_cols = df.select_dtypes(include=[np.number]).columns.tolist()
    if not num_cols:
        return []

    priority_keys = [
        "dur", "time",
        "byte", "bytes", "sbytes", "dbytes",
        "tot",   # Total bytes, flows, etc.
        "pkt", "pkts", "spkts", "dpkts",
        "rate", "bps", "pps",
        "len", "length",
    ]

    selected = []
    lower_map = {c: c.lower() for c in num_cols}

    for key in priority_keys:
        for c in num_cols:
            if c not in selected and key in lower_map[c]:
                selected.append(c)
                if len(selected) >= max_cols:
                    break
        if len(selected) >= max_cols:
            break

    if not selected:
        selected = num_cols[:max_cols]

    return selected


def pick_scatter_cols(df, priority_x=None, priority_y=None):
    """Choose two numeric columns (x, y) for a scatterplot."""
    num_cols = df.select_dtypes(include=[np.number]).columns.tolist()
    if len(num_cols) < 2:
        return None, None

    def pick(priority):
        if not priority:
            return None
        for key in priority:
            for c in num_cols:
                if key in c.lower():
                    return c
        return None

    x_col = pick(priority_x)
    if x_col is None:
        x_col = num_cols[0]

    y_col = pick(priority_y)
    if y_col is None:
        for c in num_cols:
            if c != x_col:
                y_col = c
                break

    if x_col == y_col:
        for c in num_cols:
            if c not in (x_col, y_col):
                y_col = c
                break

    if x_col == y_col:
        return None, None

    return x_col, y_col


