def build_llm4ids_model(input_dim, n_classes,
                        hidden_units=(512, 256),
                        dropout_rate=0.3,
                        l2_reg=1e-4,
                        learning_rate=1e-3):
    """Petit MLP profond sur la fusion [embeddings texte + features numériques]."""
    model = models.Sequential([
        layers.Input(shape=(input_dim,)),
        layers.Dense(hidden_units[0],
                     activation="relu",
                     kernel_regularizer=regularizers.l2(l2_reg)),
        layers.BatchNormalization(),
        layers.Dropout(dropout_rate),

        layers.Dense(hidden_units[1],
                     activation="relu",
                     kernel_regularizer=regularizers.l2(l2_reg)),
        layers.BatchNormalization(),
        layers.Dropout(dropout_rate),

        layers.Dense(n_classes, activation="softmax"),
    ])

    model.compile(
        optimizer=tf.keras.optimizers.Adam(learning_rate=learning_rate),
        loss="categorical_crossentropy",
        metrics=["accuracy"],
    )
    return model


def train_llm4ids_on_dataset(ds_dict, outdir, epochs=30, batch_size=1024):
    """Entraîne LLM4IDS sur la fusion (X_*_comb) du dataset UNSW-NB15."""
    name = ds_dict["name"]
    le   = ds_dict["le"]

    X_train = ds_dict["X_train_comb"]
    X_val   = ds_dict["X_val_comb"]
    X_test  = ds_dict["X_test_comb"]

    y_train_oh = ds_dict["y_train_oh"]
    y_val_oh   = ds_dict["y_val_oh"]
    y_test_oh  = ds_dict["y_test_oh"]

    n_classes = y_train_oh.shape[1]
    input_dim = X_train.shape[1]

    ensure_dir(outdir)
    model_path = os.path.join(outdir, f"{name}_LLM4IDS.keras")

    model = build_llm4ids_model(input_dim=input_dim, n_classes=n_classes)

    callbacks = [
        EarlyStopping(
            monitor="val_loss",
            patience=5,
            restore_best_weights=True,
        ),
        ReduceLROnPlateau(
            monitor="val_loss",
            factor=0.5,
            patience=3,
            verbose=1,
        ),
        ModelCheckpoint(
            filepath=model_path,
            monitor="val_loss",
            save_best_only=True,
            verbose=1,
        ),
    ]

    print(f"\n=== Training LLM4IDS on {name} ===")
    history = model.fit(
        X_train, y_train_oh,
        validation_data=(X_val, y_val_oh),
        epochs=epochs,
        batch_size=batch_size,
        verbose=2,
        callbacks=callbacks,
    )

    eval_name = f"LLM4IDS-{name}"
    metrics, cm, class_names, y_true_int, y_pred_int, y_score = eval_model_unv(
        model,
        X_test,
        y_test_oh,
        le=le,
        model_name=eval_name,
        keras=True,
        outdir=outdir,
    )

    # Courbes avancées
    plot_multiclass_roc_pr(y_true_int, y_score, le, eval_name, outdir)
    plot_reliability_diagram(y_true_int, y_score, eval_name, outdir)

    metrics["ModelFile"]   = model_path
    metrics["ModelSizeMB"] = get_model_size(model_path)
    metrics["InfTime_ms"]  = measure_inference_time(
        model, X_test, keras=True, n_repeats=3
    )

    return metrics


