TEST_EMB_PATH  = os.path.join(EMBED_BASE, "test_embeddings.npy")


def load_unsw_dataset():
    print("=== Loading UNSW-NB15 ===")
    df_train = pd.read_csv(UNSW_TRAIN_CSV)
    df_test  = pd.read_csv(UNSW_TEST_CSV)

    # Uniformiser les noms de colonnes
    for df in [df_train, df_test]:
        df.columns = [c.strip().lower() for c in df.columns]

    # Colonne de label
    if "attack_cat" in df_train.columns:
        df_train["label"] = df_train["attack_cat"].fillna("normal").astype(str)
        df_test["label"]  = df_test["attack_cat"].fillna("normal").astype(str)
    elif "label" in df_train.columns:
        df_train["label"] = df_train["label"].astype(str)
        df_test["label"]  = df_test["label"].astype(str)
    else:
        raise ValueError("No 'attack_cat' or 'label' in UNSW CSVs.")

    # Colonnes numériques (exclure IPs, ports, id, temps, label, attack_cat)
    id_like = [c for c in df_train.columns
               if any(k in c for k in ["srcip", "dstip", "sport", "dport", "id", "time"])]
    to_exclude = set(id_like + ["label", "attack_cat"])

    num_cols = (
        df_train.drop(columns=list(to_exclude), errors="ignore")
               .select_dtypes(include=[np.number])
               .columns
    )
    num_cols = list(num_cols)
    if len(num_cols) == 0:
        raise ValueError("No numeric columns in UNSW-NB15 – check file.")

    print(f"[UNSW] Numeric feature cols ({len(num_cols)}): {num_cols[:10]} ...")

    # Matrices numériques
    X_num_train_all = df_train[num_cols].fillna(0.0).values
    X_num_test      = df_test[num_cols].fillna(0.0).values

    y_train_all = df_train["label"].values
    y_test      = df_test["label"].values

    # --------------------------------------------------------
    # Split train / val ALIGNE (numérique seulement)
    # ( même random_state que pour les embeddings sauvegardés )
    # --------------------------------------------------------
    X_train_num, X_val_num, y_train, y_val = train_test_split(
        X_num_train_all,
        y_train_all,
        test_size=0.2,
        random_state=RANDOM_STATE,
        stratify=y_train_all
    )

    # Standardisation sur le train
    scaler = StandardScaler()
    X_train_num = scaler.fit_transform(X_train_num)
    X_val_num   = scaler.transform(X_val_num)
    X_test_num  = scaler.transform(X_num_test)

    # --------------------------------------------------------
    # CHARGEMENT DES EMBEDDINGS PRÉCALCULÉS
    # (shape : (140272, 384), (35069, 384), (82332, 384))
    # --------------------------------------------------------
    X_train_emb = np.load(TRAIN_EMB_PATH)
    X_val_emb   = np.load(VAL_EMB_PATH)
    X_test_emb  = np.load(TEST_EMB_PATH)

    # Vérifications de cohérence
    print(X_train_emb.shape, X_train_num.shape)
    assert X_train_emb.shape[0] == X_train_num.shape[0], "Train emb/num mismatch"
    assert X_val_emb.shape[0]   == X_val_num.shape[0],   "Val emb/num mismatch"
    assert X_test_emb.shape[0]  == X_test_num.shape[0],  "Test emb/num mismatch"

    # Fusion [embeddings texte + features numériques]
    X_train_comb = np.concatenate([X_train_emb, X_train_num], axis=1)
    X_val_comb   = np.concatenate([X_val_emb,   X_val_num],   axis=1)
    X_test_comb  = np.concatenate([X_test_emb,  X_test_num],  axis=1)

    # --------------------------------------------------------
    # Encodage labels
    # --------------------------------------------------------
    le = LabelEncoder()
    y_train_int = le.fit_transform(y_train)
    y_val_int   = le.transform(y_val)
    y_test_int  = le.transform(y_test)

    y_train_oh = tf.keras.utils.to_categorical(y_train_int)
    y_val_oh   = tf.keras.utils.to_categorical(y_val_int)
    y_test_oh  = tf.keras.utils.to_categorical(y_test_int)

    print(f"[UNSW] Shapes – X_train_comb: {X_train_comb.shape}, X_val_comb: {X_val_comb.shape}, X_test_comb: {X_test_comb.shape}")
    print(f"[UNSW] Classes : {list(le.classes_)}")

    return {
        "name": "UNSW-NB15",
        "num_cols": num_cols,
        "scaler": scaler,
        "le": le,
        "X_train_num": X_train_num,
        "X_val_num": X_val_num,
        "X_test_num": X_test_num,
        "X_train_emb": X_train_emb,
        "X_val_emb": X_val_emb,
        "X_test_emb": X_test_emb,
        "X_train_comb": X_train_comb,
        "X_val_comb": X_val_comb,
        "X_test_comb": X_test_comb,
        "y_train": y_train_int,
        "y_val": y_val_int,
        "y_test": y_test_int,
        "y_train_oh": y_train_oh,
        "y_val_oh": y_val_oh,
        "y_test_oh": y_test_oh,
    }


