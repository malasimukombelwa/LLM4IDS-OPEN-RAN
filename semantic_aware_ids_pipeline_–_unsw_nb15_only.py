def auto_select_kpi_cols_nids(df, max_cols: int = 6):
    """Sélectionne quelques KPI réseau parmi les colonnes numériques."""
    num_cols = df.select_dtypes(include=[np.number]).columns.tolist()
    if not num_cols:
        return []

    priority_keys = [
        "dur", "time",
        "byte", "bytes", "sbytes", "dbytes",
        "pkt", "pkts", "spkts", "dpkts",
        "rate", "bps", "pps",
        "len", "length",
    ]

    selected = []
    lower_map = {c: c.lower() for c in num_cols}

    for key in priority_keys:
        for c in num_cols:
            if c not in selected and key in lower_map[c]:
                selected.append(c)
                if len(selected) >= max_cols:
                    break
        if len(selected) >= max_cols:
            break

    # fallback si rien de très parlant
    if not selected:
        selected = num_cols[:max_cols]

    return selected


def pick_scatter_cols(df, priority_x=None, priority_y=None):
    """Choisit deux colonnes numériques (x, y) pour un scatterplot."""
    num_cols = df.select_dtypes(include=[np.number]).columns.tolist()
    if len(num_cols) < 2:
        return None, None

    def pick(priority):
        if not priority:
            return None
        for key in priority:
            for c in num_cols:
                if key in c.lower():
                    return c
        return None

    x_col = pick(priority_x)
    if x_col is None:
        x_col = num_cols[0]

    y_col = pick(priority_y)
    if y_col is None:
        for c in num_cols:
            if c != x_col:
                y_col = c
                break

    if x_col == y_col:
        for c in num_cols:
            if c not in (x_col, y_col):
                y_col = c
                break

    if x_col == y_col:
        return None, None

    return x_col, y_col


