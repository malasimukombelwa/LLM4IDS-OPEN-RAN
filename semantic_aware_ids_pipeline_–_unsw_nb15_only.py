def eval_model_unv(model, X_test, y_test, le=None,
                   model_name="Model", keras=False):
    """
    Évaluation universelle (multi-classe) pour sklearn ou Keras.
    Retourne un dict de métriques et affiche rapport + matrice de confusion.
    """
    # Harmoniser y_test
    y_true = np.array(y_test)
    if len(y_true.shape) > 1 and y_true.shape[1] > 1:
        # one-hot -> int
        y_true = np.argmax(y_true, axis=1)

    # Prédictions
    if keras:
        y_score = model.predict(X_test, verbose=0)
        y_pred = np.argmax(y_score, axis=1)
    else:
        if hasattr(model, "predict_proba"):
            y_score = model.predict_proba(X_test)
        else:
            y_score = None
        y_pred = model.predict(X_test)

    # Gestion labels / encodage
    if le is not None:
        if isinstance(y_true[0], str):
            y_true_int = le.transform(y_true)
        else:
            y_true_int = y_true

        if isinstance(y_pred[0], str):
            y_pred_int = le.transform(y_pred)
        else:
            y_pred_int = y_pred

        y_true_lbl = le.inverse_transform(y_true_int)
        y_pred_lbl = le.inverse_transform(y_pred_int)
        class_names = le.classes_
    else:
        y_true_int = y_true
        y_pred_int = y_pred
        y_true_lbl = y_true
        y_pred_lbl = y_pred
        class_names = np.unique(y_true)

    print(f"\n=== Classification report – {model_name} ===")
    print(classification_report(y_true_lbl, y_pred_lbl, digits=4))

    cm = confusion_matrix(y_true_lbl, y_pred_lbl, labels=class_names)
    plot_confusion(cm, class_names, title=f"Confusion – {model_name}")

    acc = accuracy_score(y_true_int, y_pred_int)
    prec = precision_score(y_true_int, y_pred_int, average="weighted", zero_division=0)
    rec  = recall_score(y_true_int, y_pred_int, average="weighted", zero_division=0)
    f1   = f1_score(y_true_int, y_pred_int, average="weighted", zero_division=0)

    metrics = {
        "Model": model_name,
        "Accuracy": acc,
        "Precision": prec,
        "Recall": rec,
        "F1": f1,
    }
    return metrics, cm, class_names, y_true_int, y_pred_int, y_score

def get_or_compute_embeddings(text_list, base_dir, split_name, encoder, batch_size=256):
    """
    text_list : iterable de textes (list, np.array, Series…)
    base_dir  : dossier où sauvegarder/charger les embeddings
    split_name: 'train', 'val', 'test'
    encoder   : SentenceTransformer déjà initialisé
    """
    os.makedirs(base_dir, exist_ok=True)
    cache_path = os.path.join(base_dir, f"{split_name}_embeddings.npy")

    # 1) Essayer de charger le cache
    if os.path.exists(cache_path):
        emb = np.load(cache_path)
        if emb.shape[0] == len(text_list):
            print(f"[CACHE] Loaded embeddings from {cache_path} with shape {emb.shape}")
            return emb
        else:
            print(
                f"[CACHE] Shape mismatch for {cache_path} "
                f"(cached={emb.shape[0]}, expected={len(text_list)}). Recomputing..."
            )

    # 2) Sinon : calcul
    print(f"[ENCODER] Computing embeddings for split='{split_name}' ({len(text_list)} samples)...")
    texts = list(text_list)
    emb = encoder.encode(texts, show_progress_bar=True, batch_size=batch_size)

    # 3) Sauvegarde
    np.save(cache_path, emb)
    print(f"[CACHE] Saved embeddings to {cache_path} with shape {emb.shape}")
    return emb

