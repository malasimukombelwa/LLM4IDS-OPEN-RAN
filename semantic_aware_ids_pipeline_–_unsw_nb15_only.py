def unsw_row_to_text(row):
    """
    Narration texte pour une connexion UNSW-NB15.
    Tu peux enrichir si tu veux (ajouter d'autres colonnes).
    """
    parts = []
    if "proto" in row.index:
        parts.append(f"Protocol {row['proto']}.")
    if "service" in row.index:
        parts.append(f"Service {row['service']}.")
    if "state" in row.index:
        parts.append(f"State {row['state']}.")
    for c in ["saddr", "sport", "daddr", "dport"]:
        if c in row.index:
            parts.append(f"{c}={row[c]}.")
    for c in ["sbytes", "dbytes", "spkts", "dpkts", "sttl", "dttl"]:
        if c in row.index:
            parts.append(f"{c}={row[c]}.")
    if "attack_cat" in row.index:
        parts.append(f"Attack category {row['attack_cat']}.")
    if "label" in row.index:
        parts.append(f"Label {row['label']}.")
    return " ".join(str(p) for p in parts if p)

